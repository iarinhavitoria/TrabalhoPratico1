    private void configuraTabela(List<Produto> lista) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Codigo");
        model.addColumn("Produto");
        model.addColumn("Valor da Compra");
        model.addColumn("Valor da Venda");
        model.addColumn("Estoque");

        for (Produto p : lista) {
            Vector valor = new Vector();
            valor.add(0,p.getCodProduto());
            valor.add(1,p.getNome());
            valor.add(2,p.getValorcompra());
            valor.add(3,p.getValorvenda());
            valor.add(4,p.getEstoque());
            model.addRow(valor);
        }
        tblProdutos.setModel(model);
        tblProdutos.repaint();
    }

